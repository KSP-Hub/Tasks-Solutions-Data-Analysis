	-- –ê–Ω–∞–ª–∏–∑ –ø–æ –¥–Ω—è–º –Ω–µ–¥–µ–ª–∏.  ‚Ü™ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç:
		-- –°–∞–º—ã–µ –ø–æ–ø—É–ª—è—Ä–Ω—ã–µ –¥–Ω–∏ –¥–ª—è –ø—É–±–ª–∏–∫–∞—Ü–∏–π
		-- –ö–æ–≥–¥–∞ –¶–ê –Ω–∞–∏–±–æ–ª–µ–µ –∞–∫—Ç–∏–≤–Ω–∞
		-- –ó–∞–≤–∏—Å–∏–º–æ—Å—Ç—å –ª–∞–π–∫–æ–≤ –æ—Ç –¥–Ω—è –Ω–µ–¥–µ–ª–∏
		-- –û–ø—Ä–µ–¥–µ–ª–∏–º –º–µ–¥–∏–∞–Ω—É
		-- 7-–π –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏ "–í–°/Sunday". 0 - –¥–µ–Ω—å –≤ PostgreSQL. –í—ã—Ö–æ–¥–Ω–æ–π. –ù–µ—Ç –ø–æ—Å—Ç–æ–≤.

-- 4A. –†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ –¥–Ω—è–º –Ω–µ–¥–µ–ª–∏. 
SELECT 
    TO_CHAR(date, 'Day') AS weekday, 
    -- TO_CHAR: –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –¥–∞—Ç—É –≤ –Ω–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏
    -- 'Day': —Ñ–æ—Ä–º–∞—Ç –¥–ª—è –ø–æ–ª–Ω–æ–≥–æ –Ω–∞–∑–≤–∞–Ω–∏—è –¥–Ω—è
    EXTRACT(DOW FROM date) AS day_number,
    -- DOW: –ê–±–±—Ä–µ–≤–∏–∞—Ç—É—Ä–∞ - Day Of Week. –ò–∑–≤–ª–µ–∫–∞–µ—Ç –Ω–æ–º–µ—Ä –¥–Ω—è –Ω–µ–¥–µ–ª–∏ –∫–∞–∫ —á–∏—Å–ª–æ. 
    AVG(likes_count) AS avg_likes,
    PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY likes_count) AS median_likes,
    COUNT(*) AS posts_count
FROM vk_posts_Group_Adventum
GROUP BY TO_CHAR(date, 'Day'), EXTRACT(DOW FROM date)
-- –≥—Ä—É–ø–ø–∏—Ä—É–µ–º –ø–æ weekday-–¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏ –∏ day_number-–Ω–æ–º–µ—Ä –¥–Ω—è –Ω–µ–¥–µ–ª–∏
ORDER BY day_number
;

-- 4B. –î–∏–∞–≥–Ω–æ—Å—Ç–∏—á–µ—Å–∫–∏–π –∑–∞–ø—Ä–æ—Å - —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ –¥–Ω—è–º –Ω–µ–¥–µ–ª–∏ —Å –Ω—É–ª–µ–≤—ã–º–∏ –ø–æ–∫–∞–∑–∞—Ç–µ–ª—è–º–∏. 
WITH all_days AS (
    SELECT 0 as day_num, '–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ' as day_name
    UNION SELECT 1, '–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫'
    UNION SELECT 2, '–í—Ç–æ—Ä–Ω–∏–∫'
    UNION SELECT 3, '–°—Ä–µ–¥–∞' 
    UNION SELECT 4, '–ß–µ—Ç–≤–µ—Ä–≥'
    UNION SELECT 5, '–ü—è—Ç–Ω–∏—Ü–∞'
    UNION SELECT 6, '–°—É–±–±–æ—Ç–∞'
),
post_data AS (
    SELECT 
        EXTRACT(DOW FROM date) as day_num,
        COUNT(*) as posts_count,
        ROUND(AVG(likes_count)::numeric, 1) as avg_likes,
        PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY likes_count) as median_likes
    FROM vk_posts_group_adventum
    GROUP BY EXTRACT(DOW FROM date)
)
SELECT 
    a.day_num,
    a.day_name,
    COALESCE(p.posts_count, 0) as posts_count,
    COALESCE(p.avg_likes, 0) as avg_likes,
    COALESCE(p.median_likes, 0) as median_likes
FROM all_days a
LEFT JOIN post_data p ON a.day_num = p.day_num
ORDER BY a.day_num
;



-- 5. ---------------------------------------------------------------------------
	-- –ê–Ω–∞–ª–∏–∑ –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤ –º–µ–∂–¥—É –ø–æ—Å—Ç–∞–º–∏. ‚Ü™ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç:
		-- –í—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ–∑–≤–æ–ª—è—Ç –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å, –∫–∞–∫ –≤—Ä–µ–º—è –º–µ–∂–¥—É –ø—É–±–ª–∏–∫–∞—Ü–∏—è–º–∏ –≤–ª–∏—è–µ—Ç –Ω–∞ –∏—Ö —É—Å–ø–µ—Ö !?

WITH post_intervals AS 
( 
-- CTE - –∞–±–±—Ä–µ–≤–∏–∞—Ç—É—Ä–∞ (Common Table Expression, –æ–±–æ–±—â—ë–Ω–Ω–æ–µ —Ç–∞–±–ª–∏—á–Ω–æ–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ). –í—ã—á–∏—Å–ª—è–µ—Ç—Å—è –ø–µ—Ä–µ–¥ –æ—Å–Ω–æ–≤–Ω—ã–º –∑–∞–ø—Ä–æ—Å–æ–º. –û–ø–µ—Ä–∞—Ç–æ—Ä WITH. –°—É—Ç—å/–¶–µ–ª—å: –£–ø—Ä–æ—â–∞—Ç—å —Å–ª–æ–∂–Ω—ã–µ –∑–∞–ø—Ä–æ—Å—ã. 
    SELECT 
        date,
        likes_count,
        EXTRACT(EPOCH FROM (date - LAG(date) OVER (ORDER BY date)))/3600 AS hours_diff
        -- –í—ã—á–∏—Å–ª—è–µ–º —Ä–∞–∑–Ω–∏—Ü—É –≤ —á–∞—Å–∞—Ö –º–µ–∂–¥—É —Ç–µ–∫—É—â–µ–π –∏ –ø—Ä–µ–¥—ã–¥—É—â–µ–π –ø—É–±–ª–∏–∫–∞—Ü–∏–µ–π
        	-- LAG(date) - –ø–æ–ª—É—á–∞–µ–º –¥–∞—Ç—É –ø—Ä–µ–¥—ã–¥—É—â–µ–π –ø—É–±–ª–∏–∫–∞—Ü–∏–∏
			-- EPOCH - –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª –≤ —Å–µ–∫—É–Ω–¥—ã        	
			-- /3600 - –ø–µ—Ä–µ–≤–æ–¥–∏–º —Å–µ–∫—É–Ω–¥—ã –≤ —á–∞—Å—ã
            -- AS - –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º –∏–º—è/–ø—Å–µ–≤–¥–æ–Ω–∏–º –æ–ø–µ—Ä–∞—Ü–∏–∏/–¥–µ–π—Å—Ç–≤–∏—è
    FROM vk_posts_group_adventum
)
SELECT 
    FLOOR(hours_diff/24) AS days_between,
    -- –ü–µ—Ä–µ–≤–æ–¥–∏–º —á–∞—Å—ã –≤ —Ü–µ–ª—ã–µ –¥–Ω–∏ (–æ–∫—Ä—É–≥–ª–µ–Ω–∏–µ –≤–Ω–∏–∑). –ü—Ä–∏–º–µ—Ä –Ω–∞ —Å–æ—Ç—ã—Ö: 42,35. –û–∫—Ä—É–≥–ª–µ–Ω–∏–µ –¥–æ –¥–µ—Å—è—Ç—ã—Ö:  ‚ñ≤–≤–≤–µ—Ä—Ö: 42,4; ‚ñº–≤–Ω–∏–∑: 42,3 (—Å–º. –ø—Ä–∞–≤–∏–ª–∞ –æ–∫—Ä—É–≥–ª–µ–Ω–∏—è —á–∏—Å–µ–ª)
    AVG(likes_count) AS avg_likes,
    PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY likes_count) AS median_likes,
    COUNT(*) AS posts_count,
    MIN(likes_count) AS min_likes,
    MAX(likes_count) AS max_likes,
    ROUND(STDDEV(likes_count), 2) AS std_dev_likes,
    -- ROUND: –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —á–∏—Å–ª–æ, –æ–∫—Ä—É–≥–ª—ë–Ω–Ω–æ–µ –¥–æ –æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–æ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –¥–µ—Å—è—Ç–∏—á–Ω—ã—Ö –∑–Ω–∞–∫–æ–≤
    	-- STDDEV: –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–∞–∑–±—Ä–æ—Å –¥–∞–Ω–Ω—ã—Ö –≤–æ–∫—Ä—É–≥ —Å—Ä–µ–¥–Ω–µ–≥–æ
    ROUND(AVG(hours_diff), 1) AS avg_hours_between
FROM post_intervals
WHERE hours_diff IS NOT NULL
GROUP BY days_between
ORDER BY days_between
-- –†–µ–∑—É–ª—å—Ç–∞—Ç. –ò–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è –∫–æ–ª–æ–Ω–æ–∫:
	-- 1. days_between: ‚Üê –ò–Ω—Ç–µ—Ä–≤–∞–ª –º–µ–∂–¥—É –ø—É–±–ª–∏–∫–∞—Ü–∏—è–º–∏ ‚ñ∂
		-- (0 = –ü–æ—Å—Ç—ã –≤ —Ç–æ—Ç –∂–µ –¥–µ–Ω—å (—Ä–∞–∑–Ω—ã–µ —á–∞—Å—ã); 1 = –ü–æ—Å—Ç—ã —á–µ—Ä–µ–∑ 1 –¥–µ–Ω—å; 2 = –ü–æ—Å—Ç—ã —á–µ—Ä–µ–∑ 2 –¥–Ω—è. 
			-- in –ì—Ä—É–ø–ø–∞ 0 - —ç—Ç–æ —á–∞—Å—Ç—ã–µ –ø—É–±–ª–∏–∫–∞—Ü–∏–∏, –≤—ã—Å–æ–∫–∞—è –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å | –ü–æ—Å—Ç—ã, –æ–ø—É–±–ª–∏–∫–æ–≤–∞–Ω–Ω—ã–µ –≤ —Ç–µ—á–µ–Ω–∏–µ 24 —á–∞—Å–æ–≤ –ø–æ—Å–ª–µ –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ –ø–æ—Å—Ç–∞ | –ò–Ω—Ç–µ—Ä–≤–∞–ª: 0-23 —á–∞—Å–∞; 
			-- –ì—Ä—É–ø–ø–∞ 1 - —ç—Ç–æ —Ä–µ–≥—É–ª—è—Ä–Ω—ã–µ –µ–∂–µ–¥–Ω–µ–≤–Ω—ã–µ –ø—É–±–ª–∏–∫–∞—Ü–∏–∏ | –ü–æ—Å—Ç—ã, –æ–ø—É–±–ª–∏–∫–æ–≤–∞–Ω–Ω—ã–µ –≤ —Ç–µ—á–µ–Ω–∏–µ 1 –¥–Ω—è –ø–æ—Å–ª–µ –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ –ø–æ—Å—Ç–∞ | –ò–Ω—Ç–µ—Ä–≤–∞–ª: 24-47 —á–∞—Å–æ–≤; 
			-- –ì—Ä—É–ø–ø–∞ 2 - —ç—Ç–æ –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∏–µ –ø—É–±–ª–∏–∫–∞—Ü–∏–∏ | –ü–æ—Å—Ç—ã, –æ–ø—É–±–ª–∏–∫–æ–≤–∞–Ω–Ω—ã–µ –≤ —Ç–µ—á–µ–Ω–∏–µ 2 –¥–Ω–µ–π –ø–æ—Å–ª–µ –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ –ø–æ—Å—Ç–∞ | –ò–Ω—Ç–µ—Ä–≤–∞–ª: 48-71 —á–∞—Å–∞;
