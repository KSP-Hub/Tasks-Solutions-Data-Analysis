-- 3. ---------------------------------------------------------------------------
	-- –ê–Ω–∞–ª–∏–∑ –ø–æ –≤—Ä–µ–º–µ–Ω–∏ —Å—É—Ç–æ–∫. ‚Ü™ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç:
		-- –£–∑–Ω–∞—Ç—å —Å—Ä–µ–¥–Ω–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ª–∞–π–∫–æ–≤ –ø–æ –∫–∞–∂–¥–æ–º—É —á–∞—Å—É —Å—É—Ç–æ–∫ –∏ –º–µ–¥–∏–∞–Ω—É, —á—Ç–æ–±—ã —Å–¥–µ–ª–∞—Ç—å –±–æ–ª–µ–µ —Ç–æ—á–Ω—ã–π –æ—Ç—á—ë—Ç —Å —É—á—ë—Ç–æ–º –∞–Ω–æ–º–∞–ª–∏–π
		-- –∏ –Ω–∞–∏–±–æ–ª–µ–µ –∞–∫—Ç–∏–≤–Ω—ã–µ —á–∞—Å—ã
	
SELECT 
    EXTRACT(HOUR FROM date) AS hour, -- —Å–º. —Å—Ç—Ä–æ–∫–∞ ‚Ññ22
    AVG(likes_count) AS avg_likes, -- –≤—ã—á–∏—Å–ª—è–µ–º —Å—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –Ω–∞–±–æ—Ä–∞ —á–∏—Å–µ–ª, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç NULL (0) –∑–Ω–∞—á–µ–Ω–∏—è –ø—Ä–∏ —Ä–∞—Å—á–µ—Ç–µ + –ø—Å–µ–≤–¥–æ–Ω–∏–º
    PERCENTILE_CONT(0.5) within group(order by likes_count) AS median_likes, -- –Ω–∞—Ö–æ–¥–∏–º —Å–µ—Ä–µ–¥–∏–Ω–∫—É –º–µ–∂–¥—É –¥–≤—É–º—è —Ä–∞–≤–Ω—ã–º–∏ —á–∞—Å—Ç—è–º–∏. 0.5 –æ–∑–Ω–∞—á–∞–µ—Ç - 50-–π –ø–µ—Ä—Ü–µ–Ω—Ç–∏–ª—å (–º–µ–¥–∏–∞–Ω–∞) –∏ —Å–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ 
    COUNT(*) AS posts_count -- —Å—á–∏—Ç–∞–µ–º –≤—Å–µ —Å—Ç—Ä–æ–∫–∏, –≤–∫–ª—é—á–∞—è —Ç–µ, –≥–¥–µ likes_count = 0
FROM vk_posts_Group_Adventum
GROUP BY hour -- –≥—Ä—É–ø–ø–∏—Ä–æ–≤–∫–∞ –ø–æ —á–∞—Å–∞–º
ORDER BY hour -- —Å–æ—Ä—Ç–∏—Ä—É–µ–º —á–∞—Å—ã –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
;

