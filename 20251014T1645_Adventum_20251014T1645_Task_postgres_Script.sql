	 	* FROM date - –∏–∑ –∫–∞–∫–æ–≥–æ –ø–æ–ª—è –±–µ—Ä–µ–º –¥–∞—Ç—É
	 	* AS - –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ –ø—Å–µ–≤–¥–æ–Ω–∏–º–æ–≤ –¥–ª—è —Å—Ç–æ–ª–±—Ü–∞. –ë–µ–∑ AS –Ω–∞–∑–≤–∞–Ω–∏–µ –±—ã–ª–æ –±—ã: ¬´extract¬ª
	*/
	sum(likes_count) -- sum - —Å–∫–ª–∞–¥—ã–≤–∞–µ—Ç –≤—Å–µ –∑–Ω–∞—á–µ–Ω–∏—è –≤ –≥—Ä—É–ø–ø–µ (–Ω–∞–∑–≤–∞–Ω–∏–µ —Ü–µ–ª–µ–æ–π –≥—Ä—É–ø–ø—ã)
	from vk_posts_Group_Adventum -- –ó–∞–ø—Ä–æ—Å: –û—Ç–∫—É–¥–∞ –∑–∞–±—Ä–∞—Ç—å –¥–∞–Ω–Ω—ã–µ = —Ü–µ–ª–µ–≤–æ–π —Ñ–∞–π–ª
		group by year -- –ì—Ä—É–ø–ø–∏—Ä–æ–≤–∫–∞ - —É—Å–ª–æ–≤–∏–µ
			order by year desc -- –°–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –æ—Ç max –∫ min
-- –†–µ–∑—É–ª—å—Ç–∞—Ç: 2025 –≥–æ–¥ - 862 –ª–∞–π–∫–∞, 2024 –≥–æ–¥ - 363 –ª–∞–π–∫–∞
