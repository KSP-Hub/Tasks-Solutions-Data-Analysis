-- 2. ---------------------------------------------------------------------------
	-- –ü–æ–∫–∞–∂–∏ —Å–∫–æ–ª—å–∫–æ –Ω–∞–º –ø–æ—Å—Ç–∞–≤–∏–ª–∏ –ª–∞–π–∫–æ–≤ –≤ —Ä–∞–∑—Ä–µ–∑–µ –≥–æ–¥–∞ –∏ –¥–∏–∞–ø–æ–∑–æ–Ω–µ –∏—Å—Å–ª–µ–¥–æ–≤–∞–Ω–∏—è
select 
	extract (year from date) as year, 
	/*
	 * EXTRACT() - —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —á–∞—Å—Ç–µ–π –¥–∞—Ç—ã
